#desenvolva um programa que leia as duas notas de um aluno, calcule e mostra sua média

#o aluno vdigitará as duas notas

n1 = int(input('Digite a primeira nota:'))
n2 = int (input('Digite a segunda nota:'))

#na média primeiro somar oa notas e logo apos dividir por 2     
média =(n1 + n2 )/2

#por fim mostrará a média do aluno 
print('A média do aluno é {}'.format(média))

